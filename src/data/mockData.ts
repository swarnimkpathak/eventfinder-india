export function getFilteredEvents(params: {
  city?: string;
  interests?: string[];
  zones?: string[];
  query?: string;
  priceMax?: number;
  dateFilter?: 'today' | 'weekend' | 'week' | null;
}): Event[] {
  const today = new Date();
  today.setHours(0, 0, 0, 0);

  return mockEvents.filter(event => {
    if (params.city && event.city !== params.city) return false;
    if (params.interests && params.interests.length > 0 && !params.interests.includes(event.category)) return false;
    if (params.zones && params.zones.length > 0 && !params.zones.includes(event.zone)) return false;
    if (params.priceMax !== undefined && event.price > params.priceMax) return false;
    if (params.query) {
      const q = params.query.toLowerCase();
      if (!event.title.toLowerCase().includes(q) &&
          !event.venue.toLowerCase().includes(q) &&
          !event.tags.some(t => t.toLowerCase().includes(q)) &&
          !event.zone.toLowerCase().includes(q)) {
        return false;
      }
    }
    if (params.dateFilter) {
      const eventDate = new Date(event.date);
      eventDate.setHours(0, 0, 0, 0);
      const day = today.getDay();
      const daysUntilSat = (6 - day + 7) % 7;
      const daysUntilSun = (7 - day) % 7 || 7;
      const saturday = new Date(today);
      saturday.setDate(today.getDate() + daysUntilSat);
      const sunday = new Date(today);
      sunday.setDate(today.getDate() + daysUntilSun);
      const nextWeek = new Date(today);
      nextWeek.setDate(today.getDate() + 7);

      if (params.dateFilter === 'today' && eventDate.getTime() !== today.getTime()) return false;
      if (params.dateFilter === 'weekend' && (eventDate < saturday || eventDate > sunday)) return false;
      if (params.dateFilter === 'week' && (eventDate < today || eventDate > nextWeek)) return false;
    }
    return true;
  });
}
